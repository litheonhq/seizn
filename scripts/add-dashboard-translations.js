// Use English as fallback for languages without specific translations
const defaultTranslation = dashboardTranslations.en;

const dictPath = path.join(__dirname, '../src/i18n/dictionaries');
const files = fs.readdirSync(dictPath).filter(f => f.endsWith('.json'));

files.forEach(file => {
  const lang = file.replace('.json', '');
  const filePath = path.join(dictPath, file);

  try {
    const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));

    // Get language-specific translations or use English as fallback
    const translations = dashboardTranslations[lang] || defaultTranslation;

    // Add/update dashboard translations
    if (data.dashboard) {
      data.dashboard.keysPage = translations.keysPage;
      data.dashboard.usagePage = { ...data.dashboard.usagePage, ...translations.usagePage };
      data.dashboard.organizationsPage = translations.organizationsPage;
    }

    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf-8');
    console.log(`Updated ${file}`);
  } catch (err) {
    console.error(`Error updating ${file}:`, err.message);
  }
});

console.log('Done!');
