-- SSO/SAML Foundation for Enterprise Authentication
-- Enables organizations to configure SSO providers for their members

-- ============================================
-- SSO Configuration Table
-- ============================================

-- SSO connection types
CREATE TYPE sso_provider_type AS ENUM ('saml', 'oidc');
CREATE TYPE sso_connection_status AS ENUM ('draft', 'testing', 'active', 'disabled');

-- Organization SSO configurations
CREATE TABLE IF NOT EXISTS sso_connections (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id UUID NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,

  -- Connection identity
  name VARCHAR(100) NOT NULL,
  provider_type sso_provider_type NOT NULL DEFAULT 'saml',
  status sso_connection_status NOT NULL DEFAULT 'draft',

  -- SAML Configuration (stored encrypted in production)
  -- These fields are nullable for draft status
  entity_id VARCHAR(500),           -- IdP Entity ID (e.g., https://idp.example.com/saml)
  sso_url VARCHAR(500),             -- IdP SSO URL
  slo_url VARCHAR(500),             -- IdP Single Logout URL (optional)
  certificate TEXT,                 -- IdP X.509 Certificate (PEM format)

  -- OIDC Configuration (alternative to SAML)
  oidc_issuer VARCHAR(500),         -- OIDC Issuer URL
  oidc_client_id VARCHAR(255),      -- OIDC Client ID
  oidc_client_secret_encrypted TEXT, -- OIDC Client Secret (encrypted)

  -- SP (Service Provider) Configuration - generated by Seizn
  sp_entity_id VARCHAR(500),        -- Our SP Entity ID
  sp_acs_url VARCHAR(500),          -- Assertion Consumer Service URL
  sp_metadata_url VARCHAR(500),     -- SP Metadata URL

  -- Domain mapping for automatic SSO routing
  email_domains TEXT[] DEFAULT '{}', -- e.g., ['example.com', 'corp.example.com']

  -- Attribute mapping (IdP attribute -> Seizn attribute)
  attribute_mapping JSONB DEFAULT '{
    "email": "email",
    "firstName": "first_name",
    "lastName": "last_name",
    "displayName": "display_name",
    "groups": "groups"
  }'::jsonb,

  -- Behavior settings
  settings JSONB DEFAULT '{
    "allowIdpInitiated": true,
    "forceAuthn": false,
    "signRequest": true,
    "wantAssertionsSigned": true,
    "nameIdFormat": "urn:oasis:names:tc:SAML:1.1:nameid-format:emailAddress",
    "authnContextClassRef": "urn:oasis:names:tc:SAML:2.0:ac:classes:PasswordProtectedTransport",
    "defaultRole": "member",
    "autoProvision": true,
    "jitProvisioning": true
  }'::jsonb,

  -- Audit
  created_by TEXT REFERENCES profiles(id),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  last_tested_at TIMESTAMP WITH TIME ZONE,
  last_used_at TIMESTAMP WITH TIME ZONE,

  -- Constraints
  UNIQUE(organization_id, name)
);

-- SSO Domain Verification (prevent domain hijacking)
CREATE TABLE IF NOT EXISTS sso_domain_verifications (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id UUID NOT NULL REFERENCES organizations(id) ON DELETE CASCADE,
  domain VARCHAR(255) NOT NULL,

  -- Verification methods
  verification_method VARCHAR(50) NOT NULL DEFAULT 'dns_txt',
  verification_token VARCHAR(255) NOT NULL,
  verified_at TIMESTAMP WITH TIME ZONE,

  -- Status
  is_verified BOOLEAN NOT NULL DEFAULT false,
  expires_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT (NOW() + INTERVAL '7 days'),

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

  -- One verification per domain per org
  UNIQUE(organization_id, domain)
);

-- SSO Sessions (for SAML session tracking)
CREATE TABLE IF NOT EXISTS sso_sessions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  connection_id UUID NOT NULL REFERENCES sso_connections(id) ON DELETE CASCADE,
  user_id TEXT NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,

  -- SAML session identifiers
  session_index VARCHAR(255),       -- IdP session index
  name_id VARCHAR(500),             -- SAML NameID
  name_id_format VARCHAR(255),      -- NameID format

  -- Session metadata
  idp_session_not_on_or_after TIMESTAMP WITH TIME ZONE,
  attributes JSONB DEFAULT '{}',    -- Raw SAML attributes

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  expires_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- SSO Login Attempts (for debugging and security)
CREATE TABLE IF NOT EXISTS sso_login_attempts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  connection_id UUID REFERENCES sso_connections(id) ON DELETE SET NULL,
  organization_id UUID REFERENCES organizations(id) ON DELETE SET NULL,

  -- Request details
  request_id VARCHAR(255),          -- SAML AuthnRequest ID
  relay_state TEXT,                 -- Original URL to redirect to

  -- Response details
  response_status VARCHAR(50),      -- 'success', 'error', 'pending'
  error_code VARCHAR(100),
  error_message TEXT,

  -- User info (if successful)
  user_id TEXT REFERENCES profiles(id) ON DELETE SET NULL,
  email VARCHAR(255),

  -- Metadata
  ip_address INET,
  user_agent TEXT,

  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

