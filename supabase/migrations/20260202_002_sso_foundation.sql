-- Find SSO connection by email domain
CREATE OR REPLACE FUNCTION find_sso_connection_by_email(p_email VARCHAR(255))
RETURNS TABLE (
  connection_id UUID,
  organization_id UUID,
  provider_type sso_provider_type,
  sso_url VARCHAR(500)
) AS $$
DECLARE
  v_domain VARCHAR(255);
BEGIN
  -- Extract domain from email
  v_domain := split_part(p_email, '@', 2);

  RETURN QUERY
  SELECT
    sc.id AS connection_id,
    sc.organization_id,
    sc.provider_type,
    sc.sso_url
  FROM sso_connections sc
  JOIN sso_domain_verifications dv ON dv.organization_id = sc.organization_id
  WHERE sc.status = 'active'
    AND dv.domain = v_domain
    AND dv.is_verified = true
    AND v_domain = ANY(sc.email_domains)
  LIMIT 1;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Generate SP metadata URL
CREATE OR REPLACE FUNCTION generate_sp_entity_id(p_org_slug VARCHAR(50))
RETURNS VARCHAR(500) AS $$
BEGIN
  RETURN 'https://seizn.com/api/sso/saml/' || p_org_slug || '/metadata';
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- Generate ACS URL
CREATE OR REPLACE FUNCTION generate_sp_acs_url(p_org_slug VARCHAR(50))
RETURNS VARCHAR(500) AS $$
BEGIN
  RETURN 'https://seizn.com/api/sso/saml/' || p_org_slug || '/acs';
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- Create SSO connection with auto-generated SP config
CREATE OR REPLACE FUNCTION create_sso_connection(
  p_organization_id UUID,
  p_name VARCHAR(100),
  p_provider_type sso_provider_type DEFAULT 'saml',
  p_created_by TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_connection_id UUID;
  v_org_slug VARCHAR(50);
BEGIN
  -- Get org slug for SP URLs
  SELECT slug INTO v_org_slug FROM organizations WHERE id = p_organization_id;

  INSERT INTO sso_connections (
    organization_id,
    name,
    provider_type,
    sp_entity_id,
    sp_acs_url,
    sp_metadata_url,
    created_by
  ) VALUES (
    p_organization_id,
    p_name,
    p_provider_type,
    generate_sp_entity_id(v_org_slug),
    generate_sp_acs_url(v_org_slug),
    generate_sp_entity_id(v_org_slug),
    p_created_by
  )
  RETURNING id INTO v_connection_id;

  RETURN v_connection_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Update timestamps trigger
CREATE OR REPLACE FUNCTION update_sso_connection_timestamp()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_sso_connection_updated
  BEFORE UPDATE ON sso_connections
  FOR EACH ROW
  EXECUTE FUNCTION update_sso_connection_timestamp();

