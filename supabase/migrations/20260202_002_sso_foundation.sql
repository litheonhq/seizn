-- SSO Connections: Only org admins can manage
CREATE POLICY "Org admins can manage SSO connections"
  ON sso_connections FOR ALL
  USING (
    EXISTS (
      SELECT 1 FROM organization_members om
      WHERE om.organization_id = sso_connections.organization_id
      AND om.user_id = auth.uid()::text
      AND om.role IN ('owner', 'admin')
    )
  );

-- Domain Verifications: Only org admins can manage
CREATE POLICY "Org admins can manage domain verifications"
  ON sso_domain_verifications FOR ALL
  USING (
    EXISTS (
      SELECT 1 FROM organization_members om
      WHERE om.organization_id = sso_domain_verifications.organization_id
      AND om.user_id = auth.uid()::text
      AND om.role IN ('owner', 'admin')
    )
  );

-- SSO Sessions: Users can view their own sessions, admins can view all
CREATE POLICY "Users can view their own SSO sessions"
  ON sso_sessions FOR SELECT
  USING (user_id = auth.uid()::text);

CREATE POLICY "Admins can view org SSO sessions"
  ON sso_sessions FOR SELECT
  USING (
    EXISTS (
      SELECT 1 FROM sso_connections sc
      JOIN organization_members om ON om.organization_id = sc.organization_id
      WHERE sc.id = sso_sessions.connection_id
      AND om.user_id = auth.uid()::text
      AND om.role IN ('owner', 'admin')
    )
  );

-- Login Attempts: Admins can view for their org
CREATE POLICY "Admins can view login attempts"
  ON sso_login_attempts FOR SELECT
  USING (
    EXISTS (
      SELECT 1 FROM organization_members om
      WHERE om.organization_id = sso_login_attempts.organization_id
      AND om.user_id = auth.uid()::text
      AND om.role IN ('owner', 'admin')
    )
  );

