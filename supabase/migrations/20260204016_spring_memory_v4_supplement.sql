-- Get all edges for a memory (both directions)
CREATE OR REPLACE FUNCTION get_memory_edges(
  p_memory_id UUID,
  p_edge_types TEXT[] DEFAULT NULL,
  p_direction TEXT DEFAULT 'both' -- 'outgoing', 'incoming', 'both'
)
RETURNS TABLE (
  edge_id UUID,
  other_memory_id UUID,
  edge_type VARCHAR(30),
  weight NUMERIC,
  direction TEXT,
  reason TEXT,
  created_at TIMESTAMPTZ
) AS $$
BEGIN
  RETURN QUERY
  SELECT
    e.id,
    CASE WHEN e.src_memory_id = p_memory_id THEN e.dst_memory_id ELSE e.src_memory_id END,
    e.edge_type,
    e.weight,
    CASE WHEN e.src_memory_id = p_memory_id THEN 'outgoing'::text ELSE 'incoming'::text END,
    e.reason,
    e.created_at
  FROM spring_memory_edges e
  WHERE (
    (p_direction = 'both' AND (e.src_memory_id = p_memory_id OR e.dst_memory_id = p_memory_id))
    OR (p_direction = 'outgoing' AND e.src_memory_id = p_memory_id)
    OR (p_direction = 'incoming' AND e.dst_memory_id = p_memory_id)
  )
  AND (p_edge_types IS NULL OR e.edge_type = ANY(p_edge_types))
  ORDER BY e.weight DESC, e.created_at DESC;
END;
$$ LANGUAGE plpgsql STABLE;

-- Get canonical memory (follow supersedes chain)
CREATE OR REPLACE FUNCTION get_canonical_memory(p_memory_id UUID)
RETURNS UUID AS $$
DECLARE
  current_id UUID := p_memory_id;
  next_id UUID;
  iteration INT := 0;
BEGIN
  LOOP
    SELECT superseded_by INTO next_id
    FROM spring_memory_notes
    WHERE id = current_id AND superseded_by IS NOT NULL;

    IF next_id IS NULL THEN
      RETURN current_id;
    END IF;

    current_id := next_id;
    iteration := iteration + 1;

    -- Prevent infinite loops (max 100 hops)
    IF iteration > 100 THEN
      RETURN current_id;
    END IF;
  END LOOP;
END;
$$ LANGUAGE plpgsql STABLE;

-- Find contradicting memories
CREATE OR REPLACE FUNCTION find_contradicting_memories(p_memory_id UUID)
RETURNS TABLE (
  contradicting_id UUID,
  content TEXT,
  reason TEXT,
  confidence NUMERIC
) AS $$
BEGIN
  RETURN QUERY
  SELECT
    n.id,
    n.content,
    e.reason,
    e.confidence
  FROM spring_memory_edges e
  JOIN spring_memory_notes n ON (
    (e.src_memory_id = p_memory_id AND n.id = e.dst_memory_id)
    OR (e.dst_memory_id = p_memory_id AND n.id = e.src_memory_id)
  )
  WHERE e.edge_type = 'contradicts'
    AND n.status = 'active'
    AND n.deleted_at IS NULL
  ORDER BY e.confidence DESC, e.created_at DESC;
END;
$$ LANGUAGE plpgsql STABLE;

