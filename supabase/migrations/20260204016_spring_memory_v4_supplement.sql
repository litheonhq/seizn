-- =============================================================================
-- 4. CONTENT HASH TRIGGER
-- =============================================================================

CREATE OR REPLACE FUNCTION set_spring_content_hash()
RETURNS TRIGGER AS $$
BEGIN
  NEW.content_hash = encode(digest(COALESCE(NEW.content, ''), 'sha256'), 'hex');
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_spring_notes_content_hash') THEN
    CREATE TRIGGER trg_spring_notes_content_hash
    BEFORE INSERT OR UPDATE OF content ON spring_memory_notes
    FOR EACH ROW EXECUTE FUNCTION set_spring_content_hash();
  END IF;
END $$;

-- =============================================================================
-- 5. USAGE TRACKING TRIGGER (updates used_count, last_used_at)
-- =============================================================================

CREATE OR REPLACE FUNCTION bump_spring_memory_usage()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE spring_memory_notes
  SET
    used_count = used_count + 1,
    last_used_at = NEW.created_at
  WHERE id = NEW.note_id;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_trigger WHERE tgname = 'trg_spring_usage_bump') THEN
    CREATE TRIGGER trg_spring_usage_bump
    AFTER INSERT ON spring_memory_usage
    FOR EACH ROW EXECUTE FUNCTION bump_spring_memory_usage();
  END IF;
END $$;

