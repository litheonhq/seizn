-- 6. DELETE BY FILTER FUNCTION
-- =============================================================================

CREATE OR REPLACE FUNCTION delete_spring_memories_by_filter(
  p_user_id TEXT,
  p_filters JSONB DEFAULT '{}'::jsonb
) RETURNS INT AS $$
DECLARE
  types_filter TEXT[];
  categories_filter TEXT[];
  namespace_filter TEXT;
  before_ts TIMESTAMPTZ;
  after_ts TIMESTAMPTZ;
  affected INT := 0;
BEGIN
  -- Extract filters
  types_filter := ARRAY(SELECT jsonb_array_elements_text(p_filters->'types'));
  categories_filter := ARRAY(SELECT jsonb_array_elements_text(p_filters->'categories'));
  namespace_filter := p_filters->>'namespace';
  before_ts := (p_filters->>'before')::timestamptz;
  after_ts := (p_filters->>'after')::timestamptz;

  -- Soft delete matching memories (v3 compatible)
  UPDATE spring_memory_notes
  SET
    status = 'deleted',
    updated_at = now()
  WHERE user_id = p_user_id
    AND status != 'deleted'
    AND (array_length(types_filter, 1) IS NULL OR note_type = ANY(types_filter))
    AND (array_length(categories_filter, 1) IS NULL OR categories && categories_filter)
    AND (namespace_filter IS NULL OR scope = namespace_filter)
    AND (before_ts IS NULL OR created_at < before_ts)
    AND (after_ts IS NULL OR created_at > after_ts);

  GET DIAGNOSTICS affected = ROW_COUNT;
  RETURN affected;
END;
$$ LANGUAGE plpgsql;

