-- Accept a candidate (move to main table)
CREATE OR REPLACE FUNCTION accept_memory_candidate(
  p_candidate_id UUID,
  p_reviewer_id TEXT,
  p_reason TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_candidate spring_memory_candidates;
  v_note_id UUID;
BEGIN
  -- Get candidate
  SELECT * INTO v_candidate
  FROM spring_memory_candidates
  WHERE id = p_candidate_id AND action = 'pending';

  IF v_candidate.id IS NULL THEN
    RAISE EXCEPTION 'Candidate not found or already processed';
  END IF;

  -- Insert into main table (using v3 column names)
  INSERT INTO spring_memory_notes (
    user_id, workspace_id, content, note_type, status,
    scope, confidence, payload_json, embedding
  )
  VALUES (
    v_candidate.user_id, v_candidate.workspace_id, v_candidate.content,
    v_candidate.note_type, 'active',
    v_candidate.scope, v_candidate.confidence, v_candidate.metadata, v_candidate.embedding
  )
  RETURNING id INTO v_note_id;

  -- Update candidate status
  UPDATE spring_memory_candidates
  SET
    action = 'accepted',
    reviewer_user_id = p_reviewer_id,
    reviewed_at = now(),
    decision_reason = p_reason,
    accepted_note_id = v_note_id
  WHERE id = p_candidate_id;

  RETURN v_note_id;
END;
$$ LANGUAGE plpgsql;

-- Reject a candidate
CREATE OR REPLACE FUNCTION reject_memory_candidate(
  p_candidate_id UUID,
  p_reviewer_id TEXT,
  p_reason TEXT DEFAULT NULL
)
RETURNS BOOLEAN AS $$
BEGIN
  UPDATE spring_memory_candidates
  SET
    action = 'rejected',
    reviewer_user_id = p_reviewer_id,
    reviewed_at = now(),
    decision_reason = p_reason
  WHERE id = p_candidate_id AND action = 'pending';

  RETURN FOUND;
END;
$$ LANGUAGE plpgsql;

-- Batch expire old pending candidates
CREATE OR REPLACE FUNCTION expire_old_candidates(p_older_than INTERVAL DEFAULT '7 days')
RETURNS INT AS $$
DECLARE
  affected INT;
BEGIN
  UPDATE spring_memory_candidates
  SET
    action = 'rejected',
    decision_reason = 'Auto-expired after ' || p_older_than::text
  WHERE action = 'pending'
    AND (
      (expires_at IS NOT NULL AND expires_at < now())
      OR (expires_at IS NULL AND created_at < now() - p_older_than)
    );

  GET DIAGNOSTICS affected = ROW_COUNT;
  RETURN affected;
END;
$$ LANGUAGE plpgsql;

