-- 9. RLS POLICIES FOR NEW TABLES
-- =============================================================================

ALTER TABLE spring_memory_candidates ENABLE ROW LEVEL SECURITY;
ALTER TABLE spring_memory_edges ENABLE ROW LEVEL SECURITY;

-- Candidates policies
CREATE POLICY "Users can view their own candidates"
  ON spring_memory_candidates FOR SELECT
  USING (user_id = auth.uid()::text);

CREATE POLICY "Users can manage their own candidates"
  ON spring_memory_candidates FOR ALL
  USING (user_id = auth.uid()::text);

-- Edges policies (through memory ownership)
CREATE POLICY "Users can view edges for their memories"
  ON spring_memory_edges FOR SELECT
  USING (
    EXISTS (
      SELECT 1 FROM spring_memory_notes n
      WHERE n.id = src_memory_id AND n.user_id = auth.uid()::text
    )
    OR EXISTS (
      SELECT 1 FROM spring_memory_notes n
      WHERE n.id = dst_memory_id AND n.user_id = auth.uid()::text
    )
  );

CREATE POLICY "Users can create edges for their memories"
  ON spring_memory_edges FOR INSERT
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM spring_memory_notes n
      WHERE n.id = src_memory_id AND n.user_id = auth.uid()::text
    )
  );

CREATE POLICY "Users can delete edges for their memories"
  ON spring_memory_edges FOR DELETE
  USING (
    EXISTS (
      SELECT 1 FROM spring_memory_notes n
      WHERE n.id = src_memory_id AND n.user_id = auth.uid()::text
    )
  );

