-- =============================================================================
-- 1. MEMORY CANDIDATES (Review Queue)
-- =============================================================================
-- Stores memories pending user approval before being committed to main table

CREATE TABLE spring_memory_candidates (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id TEXT NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  organization_id UUID REFERENCES organizations(id) ON DELETE CASCADE,
  workspace_id UUID, -- Optional workspace scoping

  -- Scope context
  namespace TEXT NOT NULL DEFAULT 'default',
  scope VARCHAR(20) NOT NULL DEFAULT 'user',
  session_id TEXT,
  agent_id TEXT,

  -- Content
  content TEXT NOT NULL,
  embedding vector(1536),
  note_type VARCHAR(50) NOT NULL DEFAULT 'fact',
  tags TEXT[] DEFAULT '{}',
  categories TEXT[] DEFAULT '{}',
  metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
  provenance JSONB NOT NULL DEFAULT '{}'::jsonb,
  confidence NUMERIC(3,2) NOT NULL DEFAULT 0.80,

  -- Review state
  action VARCHAR(20) NOT NULL DEFAULT 'pending'
    CHECK (action IN ('pending', 'accepted', 'rejected', 'redacted', 'merged')),
  reviewer_user_id TEXT REFERENCES profiles(id),
  reviewed_at TIMESTAMPTZ,
  decision_reason TEXT,

  -- If accepted, link to created note
  accepted_note_id UUID REFERENCES spring_memory_notes(id) ON DELETE SET NULL,

  -- Source tracking
  source_type TEXT DEFAULT 'ingestion'
    CHECK (source_type IN ('ingestion', 'extraction', 'import', 'agent', 'mcp')),
  source_id TEXT, -- Job ID, agent ID, etc.

  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  expires_at TIMESTAMPTZ -- Auto-reject after expiration
);

-- Indexes for candidate management
CREATE INDEX IF NOT EXISTS idx_candidates_user_action
  ON spring_memory_candidates(user_id, action, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_candidates_namespace
  ON spring_memory_candidates(user_id, namespace, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_candidates_pending
  ON spring_memory_candidates(user_id, created_at DESC)
  WHERE action = 'pending';
CREATE INDEX IF NOT EXISTS idx_candidates_expired
  ON spring_memory_candidates(expires_at)
  WHERE action = 'pending' AND expires_at IS NOT NULL;

-- =============================================================================
-- 2. MEMORY EDGES (Graph Relationships)
-- =============================================================================
-- Represents relationships between memories for graph-based recall

CREATE TABLE spring_memory_edges (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  -- Edge endpoints
  src_memory_id UUID NOT NULL REFERENCES spring_memory_notes(id) ON DELETE CASCADE,
  dst_memory_id UUID NOT NULL REFERENCES spring_memory_notes(id) ON DELETE CASCADE,

  -- Edge type
  edge_type VARCHAR(30) NOT NULL CHECK (edge_type IN (
    'relates_to',    -- General relationship
    'supports',      -- Provides evidence for
    'contradicts',   -- Conflicts with
    'supersedes',    -- Replaces/updates
    'derived_from',  -- Created from
    'mentions',      -- References
    'part_of',       -- Component of
    'causes',        -- Causal relationship
    'similar_to'     -- High semantic similarity
  )),

  -- Relationship strength
  weight NUMERIC(3,2) NOT NULL DEFAULT 1.00 CHECK (weight >= 0 AND weight <= 1),

  -- Explanation
  reason TEXT,
  confidence NUMERIC(3,2) DEFAULT 0.80,

  -- Source of edge
  created_by TEXT REFERENCES profiles(id),
  created_by_agent TEXT,
  created_by_system BOOLEAN DEFAULT false, -- Auto-generated by consolidation

  -- Audit
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  -- Prevent duplicate edges
  UNIQUE(src_memory_id, dst_memory_id, edge_type)
);

-- Indexes for graph traversal
CREATE INDEX IF NOT EXISTS idx_edges_src ON spring_memory_edges(src_memory_id);
CREATE INDEX IF NOT EXISTS idx_edges_dst ON spring_memory_edges(dst_memory_id);
CREATE INDEX IF NOT EXISTS idx_edges_type ON spring_memory_edges(edge_type);
CREATE INDEX IF NOT EXISTS idx_edges_src_type ON spring_memory_edges(src_memory_id, edge_type);
CREATE INDEX IF NOT EXISTS idx_edges_dst_type ON spring_memory_edges(dst_memory_id, edge_type);

